export class MySQLFamilyQuotation extends Quotation {
  quoteIdentifier(identifier, forceQuotation) {
    if (
      forceQuotation === true ||
      KEYWORDS.filter((keyword) => keyword === identifier.toUpperCase())
        .length > 0
    ) {
      return `\`${identifier}\``;
    }
    const plainIdentifier = identifier.match(/^([^\x20-\x7F]|[\w])+$/g);
    if (plainIdentifier && !this.containsUpperChar(identifier)) {
      return identifier;
    }
    return `\`${identifier}\``;
  }

  isUpper(character) {
    return (
      character !== character.toLowerCase() &&
      character === character.toUpperCase()
    );
  }

  containsUpperChar(string) {
    for (let i = 0; i < string.length; i++) {
      if (this.isUpper(string[i])) {
        return true;
      }
    }
    return false;
  }

  quoteComment(comment) {
    const lineBreakedIndex = comment && comment.indexOf(`\n`);
    return lineBreakedIndex && lineBreakedIndex > 0
      ? `/* ${comment} */`
      : `/* ${comment} */`;
  }

  quoteLiteral(literal) {
    return `'${literal ? literal.replace(/'/g, "''") : ""}'`;
  }
}
